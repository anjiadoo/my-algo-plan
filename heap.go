/*
 * ============================================================================
 *                   📘 堆（Heap）算法全集 · 核心记忆框架
 * ============================================================================
 * 【一句话理解堆】
 *
 *   堆 = 用数组实现的完全二叉树，通过「上浮」和「下沉」两个操作维护
 *   父节点与子节点之间的大小关系，实现 O(log n) 的插入和 O(log n) 的删除最值。
 *
 *   判断能否用堆，回答一个问题：
 *     ❓ 是否需要动态维护一组数据中的最大值/最小值？
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【完全二叉树的数组表示】
 *
 *   节点索引从 0 开始，有以下关系：
 *
 *   ┌─────────────────────────────────────────────────────────────────────┐
 *   │  父节点 → 左子节点：  left  = 2*i + 1                               │
 *   │  父节点 → 右子节点：  right = 2*i + 2                               │
 *   │  子节点 → 父节点：    parent = (i - 1) / 2                          │
 *   └─────────────────────────────────────────────────────────────────────┘
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【为什么最后一个非叶子节点的索引是 n/2 - 1？】
 *
 *   核心推导：最后一个非叶子节点 = 最后一个节点的父节点
 *
 *   原因：完全二叉树从左到右填充，最后一个拥有子节点的节点，
 *        它的子节点一定包含数组中的最后一个元素。
 *
 *   数学推导：
 *     最后一个节点的索引 = n - 1
 *     它的父节点索引 = (n - 1 - 1) / 2 = (n - 2) / 2 = n/2 - 1（整数除法）
 *
 *   具体例子（n=4, 数组 [1, 8, 5, 81]）：
 *
 *            [1]        ← 索引 0（非叶子）
 *           /    \
 *         [8]    [5]    ← 索引 1（非叶子）、索引 2（叶子）
 *         /
 *       [81]            ← 索引 3（叶子）
 *
 *     最后一个节点：索引 3（值 81）
 *     它的父节点：(3-1)/2 = 1（值 8）
 *     公式验证：n/2 - 1 = 4/2 - 1 = 1 ✅
 *
 *   为什么索引 2 是叶子？
 *     索引 2 的左子节点应在 2*2+1=5，但数组长度只有 4，索引 5 不存在，
 *     所以索引 2 没有子节点，是叶子。
 *
 *   一句话总结：
 *     最后一个非叶子节点 = 最后一个元素的父节点，
 *     用父节点公式 (i-1)/2 代入 i=n-1，得到 n/2-1。
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【两个核心操作】
 *
 *   ┌─────────────────────────────────────────────────────────────────────┐
 *   │ 操作1：下沉（siftDown）                                              │
 *   │   场景：当前节点比子节点大（小顶堆）或比子节点小（大顶堆）              │
 *   │   做法：与最小（或最大）的子节点交换，递归向下调整                      │
 *   │   用途：Pop 删除堆顶后调整、建堆时从下往上调整                         │
 *   │                                                                     │
 *   │ 操作2：上浮（siftUp）                                                │
 *   │   场景：当前节点比父节点小（小顶堆）或比父节点大（大顶堆）              │
 *   │   做法：与父节点交换，循环向上调整                                    │
 *   │   用途：Push 插入新元素后调整                                         │
 *   └─────────────────────────────────────────────────────────────────────┘
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【三种常见操作的时间复杂度】
 *
 *   操作           时间复杂度       说明
 *   ──────────────────────────────────────────────────────────────────────
 *   建堆(Init)     O(n)            从最后一个非叶子节点开始逐个下沉
 *   插入(Push)     O(log n)        末尾插入 + 上浮
 *   删除堆顶(Pop)  O(log n)        堆顶与末尾交换 + 下沉
 *   查看堆顶       O(1)            直接返回 array[0]
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【建堆为什么是 O(n) 而不是 O(n log n)？】
 *
 *   直觉上每个节点下沉 O(log n)，n 个节点就是 O(n log n)？
 *   但实际上：越靠近底部的节点越多，而它们下沉的距离越短。
 *
 *   精确计算：
 *     第 k 层有 2^k 个节点，每个最多下沉 (h-k) 层（h 为树高）
 *     总操作 = Σ 2^k * (h-k) = O(n)
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【堆的典型应用场景】
 *
 *   1. Top-K 问题：维护大小为 K 的堆，求第 K 大/小的元素
 *   2. 合并 K 个有序序列：K 路归并用最小堆
 *   3. 优先级队列：任务调度、Dijkstra 最短路径
 *   4. 堆排序：建堆 + 逐个弹出堆顶
 *   5. 中位数维护：一个大顶堆 + 一个小顶堆
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【防错清单】—— 每次手写堆后对照检查
 *
 *     ✅ 建堆起点：是从 n/2-1 开始向前遍历（不是从 0 开始）？
 *     ✅ 下沉比较：小顶堆找最小子节点，大顶堆找最大子节点？
 *     ✅ 边界检查：left < n 和 right < n 是否都判断了？
 *     ✅ Pop 操作：是先交换堆顶和末尾，再缩减数组，最后下沉？
 *     ✅ Push 操作：是先 append 到末尾，再上浮？
 *     ✅ 上浮终止：当前节点 >= 父节点（小顶堆）时停止？
 *     ✅ 下沉终止：当前节点已经是三者中最小的（小顶堆）时停止？
 *     ✅ 空堆处理：Pop 时是否判断了 len == 0？
 *
 * ────────────────────────────────────────────────────────────────────────────
 * 【API 速查】
 *
 *     MinHeap（小顶堆）：
 *       1. Init(nums []int)           // 原地建堆，O(n)
 *       2. Push(x int)                // 插入元素，O(log n)
 *       3. Pop() (int, bool)          // 弹出最小值，O(log n)
 *       4. siftDown(i, n int)         // 下沉操作（内部方法）
 *       5. siftUp(i int)              // 上浮操作（内部方法）
 *
 * ============================================================================
 */

package main

import "fmt"

// MinHeap 小顶堆，基于完全二叉树结构实现
type MinHeap struct {
	array []int
}

func (h *MinHeap) Init(nums []int) {
	// 从最后一个非叶子节点开始向上构建最小堆（索引 n/2-1）
	// 原理：叶子节点天然满足堆性质，只需调整非叶子节点（详见文件头部注释）
	h.array = nums
	for i := len(h.array)/2 - 1; i >= 0; i-- {
		h.siftDown(i, len(h.array))
	}
}

func (h *MinHeap) Push(x int) {
	// 末尾加入新元素，然后上浮到正确位置
	h.array = append(h.array, x)
	h.siftUp(len(h.array) - 1)
}

func (h *MinHeap) Pop() (int, bool) {
	if len(h.array) == 0 {
		return -1, false
	}
	minVal := h.array[0]
	last := len(h.array) - 1

	// 将堆顶与末尾元素交换，缩减堆大小
	h.array[0], h.array[last] = h.array[last], h.array[0]
	h.array = h.array[:last]

	// 下沉调整
	if len(h.array) > 0 {
		h.siftDown(0, len(h.array))
	}
	return minVal, true
}

// siftDown 下沉操作，将索引 i 处的元素下沉到合适位置
func (h *MinHeap) siftDown(i, n int) {
	minIndex := i    // 假设当前节点是最小的
	left := 2*i + 1  // 左子节点索引
	right := 2*i + 2 // 右子节点索引

	if left < n && h.array[left] < h.array[minIndex] {
		minIndex = left
	}

	if right < n && h.array[right] < h.array[minIndex] {
		minIndex = right
	}

	// 如果最小值不是当前节点，交换并递归调整
	// 交换可能破坏子树的堆性质，所以需要递归下沉
	if minIndex != i {
		h.array[i], h.array[minIndex] = h.array[minIndex], h.array[i]
		h.siftDown(minIndex, n)
	}
}

// siftUp 上浮操作，将索引 i 处的元素上浮到合适位置
func (h *MinHeap) siftUp(i int) {
	for i > 0 {
		parent := (i - 1) / 2 // 父节点索引
		if h.array[i] >= h.array[parent] {
			// 堆性质满足，停止上浮
			break
		}
		h.array[i], h.array[parent] = h.array[parent], h.array[i]
		i = parent
	}
}

func main() {
	heap := MinHeap{}
	heap.Init([]int{1, 8, 5, 81})

	fmt.Println(heap.Pop())
	fmt.Println(heap.Pop())
	heap.Push(6)
	heap.Push(9)
	fmt.Println(heap.Pop())
	fmt.Println(heap.Pop())
	fmt.Println(heap.Pop())
	fmt.Println(heap.Pop())
	fmt.Println(heap.Pop())
}
